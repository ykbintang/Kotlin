package com.dicoding.kotlin

// ## Tipe Data Character
/*
    => Data karakter (huruf), di Kotlin direpresentasikan oleh tipe Char.
    => Untuk membuat data Char, di Kotlin kita bisa menggunakan tanda petik satu (')
 */

fun main() {

    // Contoh char
    var char1: Char = 'Y';
    var char2: Char = '4';
    var char3: Char = 'Y';
    var char4: Char = '3';
    var char5: Char = 'S';

    print(char1)
    print(char2)
    print(char3)
    print(char4)
    print(char5)

    // output: Y4Y3S

}